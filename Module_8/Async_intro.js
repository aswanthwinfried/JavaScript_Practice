// What is Synchronous and
// Asynchronous Programming ?
// Synchronous Programming means it will use a single-thread, so only
// one operation of program will run at a time
// Sync is blocking - it will only send the server one request at a time and will
// wait for that request to be answered by the server.
// Asynchronous is non-blocking, which means it will send multiple
// requests to a server at a time